///////////////////////////////////////////////////////////////////////////////
//! Simple kernel to modify vertex positions in sine wave pattern
//! @param data  data in global memory
///////////////////////////////////////////////////////////////////////////////
__global__ void simple_vbo_kernel(float4 *pos, SwarmAgent *agents, unsigned int agentCount)
{
    unsigned int x = blockIdx.x*blockDim.x + threadIdx.x;

    if (x < agentCount)
    {
        SwarmAgent &temp = agents[x];

        // calculate uv coordinates
        float u = temp.position.x / temp.maxPosition().x;
        float v = temp.position.y / temp.maxPosition().y;
        u = u*2.0f - 1.0f;
        v = v*2.0f - 1.0f;

        // write output vertex
        pos[x] = make_float4(u, 0.0f, v, 1.0f);
    }
}


void launch_kernel(float4 *pos)
{
    // execute the kernel
    dim3 block(1024, 1, 1);
    dim3 grid(dSwarm.size() / block.x + dSwarm.size() % block.x ? 1 : 0, 1, 1);
    simple_vbo_kernel<<< grid, block>>>(pos, thrust::raw_pointer_cast(dSwarm.data()), dSwarm.size());
}

bool checkHW(char *name, const char *gpuType, int dev)
{
    cudaDeviceProp deviceProp;
    cudaGetDeviceProperties(&deviceProp, dev);
    strcpy(name, deviceProp.name);

    if (!STRNCASECMP(deviceProp.name, gpuType, strlen(gpuType)))
    {
        return true;
    }
    else
    {
        return false;
    }
}

int findGraphicsGPU(char *name)
{
    int nGraphicsGPU = 0;
    int deviceCount = 0;
    bool bFoundGraphics = false;
    char firstGraphicsName[256], temp[256];

    cudaError_t error_id = cudaGetDeviceCount(&deviceCount);

    if (error_id != cudaSuccess)
    {
        printf("cudaGetDeviceCount returned %d\n-> %s\n", (int)error_id, cudaGetErrorString(error_id));
        printf("> FAILED %s sample finished, exiting...\n", sSDKsample);
        exit(EXIT_FAILURE);
    }

    // This function call returns 0 if there are no CUDA capable devices.
    if (deviceCount == 0)
    {
        printf("> There are no device(s) supporting CUDA\n");
        return false;
    }
    else
    {
        printf("> Found %d CUDA Capable Device(s)\n", deviceCount);
    }

    for (int dev = 0; dev < deviceCount; ++dev)
    {
        bool bGraphics = !checkHW(temp, (const char *)"Tesla", dev);
        printf("> %s\t\tGPU %d: %s\n", (bGraphics ? "Graphics" : "Compute"), dev, temp);

        if (bGraphics)
        {
            if (!bFoundGraphics)
            {
                strcpy(firstGraphicsName, temp);
            }

            nGraphicsGPU++;
        }
    }

    if (nGraphicsGPU)
    {
        strcpy(name, firstGraphicsName);
    }
    else
    {
        strcpy(name, "this hardware");
    }

    return nGraphicsGPU;
}

